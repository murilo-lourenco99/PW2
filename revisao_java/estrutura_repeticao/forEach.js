/* For Each:
    Uso de ForEach como vetor para identificar as cores e criar uma lista de presença
*/

let cores = ["Azul", "Dourado", "Vermelho"];

cores.forEach(function(cor) {
  console.log(cor);
});

// lista de presença
let alunos = ["murilo", "Mica", "Raul", "Estefani", "Emilly"];

alunos.forEach(function(aluno) {
  console.log(aluno, ": presente");
});