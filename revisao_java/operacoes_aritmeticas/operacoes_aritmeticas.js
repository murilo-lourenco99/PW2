/* Operações Aritméticas
    Uso de Operadores Lógicos para:
     Soma, Subtração, Divisão, Multiplicação, Resto, Raiz, Potencia (^).
*/



// Calculo subtração
let menos1 = 50;
let menos2 = 100;
let resultado_menos = menos1 - menos2;
console.log("Resultado da Subtração: ",resultado_menos);

// Calculo divisão
let div1 = 10;
let div2 = 5;
let resultado_div = div1 / div2;
console.log("Resultado da Divisão: ",resultado_div);

// Calculo multiplicação
let multi1 = 15;
let multi2 = 5;
let resultado_multi = multi1 * multi2;
console.log("Resultado da Multiplicação: ",resultado_multi);

// Calculo Resto
let resto1 = 10;
let resto2 = 3;
let resultado_resto = resto1 % resto2;
console.log("Resultado do calculo do resto: ",resultado_resto);

// Calculo Raiz
let raiz = Math.sqrt(25);
console.log("Resultado da Raiz: ",raiz);

// Calculo Potencia (^)
let base = 2;
let expoente = 4;
let resultado_potencia = Math.pow(base, expoente); // o math.pow calcua a potencia sozinho, parecido com o math.sqrt
console.log("Resultado da Potência: ",resultado_potencia);